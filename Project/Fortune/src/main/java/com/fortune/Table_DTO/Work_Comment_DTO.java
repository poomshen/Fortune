package com.fortune.Table_DTO;

public class Work_Comment_DTO {
	private int work_comment_no;
	private int schedule_no;
	private String user_id;
	private String work_comment_text;
	private String work_comment_date;
	
	
	public int getWork_comment_no() {
		return work_comment_no;
	}
	public void setWork_comment_no(int work_comment_no) {
		this.work_comment_no = work_comment_no;
	}
	public int getSchedule_no() {
		return schedule_no;
	}
	public void setSchedule_no(int schedule_no) {
		this.schedule_no = schedule_no;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getWork_comment_text() {
		return work_comment_text;
	}
	public void setWork_comment_text(String work_comment_text) {
		this.work_comment_text = work_comment_text;
	}
	public String getWork_comment_date() {
		return work_comment_date;
	}
	public void setWork_comment_date(String work_comment_date) {
		this.work_comment_date = work_comment_date;
	}
	
	
}
